import os
import tempfile

import streamlit as st

from llama_index import (
    ServiceContext,
    StorageContext,
    VectorStoreIndex,
    download_loader,
)
from llama_index.chat_engine.simple import SimpleChatEngine
from llama_index.llms import OpenAI
from llama_index.vector_stores.couchbase_store import CouchbaseVectorStore


def store_document(uploaded_file, storage_context, service_context):
    """Chunk the PDF & store it in Couchbase Vector Store."""
    if uploaded_file is not None:
        temp_dir = tempfile.TemporaryDirectory()
        temp_file_path = os.path.join(temp_dir.name, uploaded_file.name)

        with open(temp_file_path, "wb") as f:
            f.write(uploaded_file.getvalue())

        PDFReader = download_loader("PDFReader", custom_path=temp_dir.name)

        loader = PDFReader()
        documents = loader.load_data(file=temp_file_path)

        index = VectorStoreIndex.from_documents(
            documents,
            service_context=service_context,
            storage_context=storage_context,
        )
        st.info(f"PDF loaded into vector store in {len(documents)} documents")
        return index
    return None


@st.cache_resource(show_spinner="Connecting to Couchbase")
def get_vector_store(
    db_conn_str,
    db_username,
    db_password,
    db_bucket,
    db_scope,
    db_collection,
    index_name,
):
    """Return the Couchbase vector store."""
    return CouchbaseVectorStore(
        connection_string=db_conn_str,
        db_username=db_username,
        db_password=db_password,
        bucket_name=db_bucket,
        scope_name=db_scope,
        collection_name=db_collection,
        index_name=index_name,
    )


